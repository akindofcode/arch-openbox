# update pacman packages
sudo pacman -Syu --noconfirm 

echo "################################################################"
echo "################### pacman packages updated ####################"
echo "################################################################"

# clean pacman cache
sudo pacman -Sc --noconfirm --clean --clean

echo "################################################################"
echo "#################### pacman cache cleaned ######################"
echo "################################################################"

# update aur packages
packer -Syu --noconfirm --noedit --auronly

echo "################################################################"
echo "##################### aur packages updated #####################"
echo "################################################################"